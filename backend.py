def remover_duplicadas_csv(arquivo_origem):
    try:
        df = pd.read_csv(arquivo_origem)
        # Identifica e remove linhas duplicadas
        df_limpo = df.drop_duplicates()

        if len(df) > len(df_limpo):
            df_limpo.to_csv(arquivo_origem, index=False, encoding='utf-8')
            print(f"✅ Duplicatas removidas. Arquivo atualizado: {arquivo_origem}. {len(df) - len(df_limpo)} linhas duplicadas foram removidas.")
        else:
            print(f"ℹ️ Nenhuma duplicata encontrada no arquivo: {arquivo_origem}.")
    except FileNotFoundError:
        print(f"Erro: Arquivo '{arquivo_origem}' não encontrado.")
    except Exception as e:
        print(f"Ocorreu um erro ao remover duplicatas: {e}")

