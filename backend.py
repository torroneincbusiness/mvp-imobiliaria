import google.generativeai as genai
from pydantic import BaseModel, Field
from typing import List
import json
import os

# --- 1. CONFIGURAÇÃO ---
genai.configure(api_key=os.getenv("GOOGLE_API_KEY")) # Usando a chave já presente no notebook
model = genai.GenerativeModel('gemini-3.5-flash') # Mantendo o modelo 'gemini-3.5-flash' que está funcionando

# --- 2. CONFIGURAÇÃO DE NEGÓCIO (Pesos e Regras) ---
PESOS = {
    "urgencia": {"alta": 50, "media": 25, "baixa": 10},
    "capacidade_financeira": {"alta": 40, "media": 20, "baixa": 5},
    "bonus_tag_premium": 10  # Bônus para tags de alto valor
}

# --- 3. BANCO DE DADOS (Simulação) ---
estoque = [
    {"id": 1, "titulo": "Apto com Varanda Gourmet", "tags": ["varanda", "escritorio"], "preco": 450000},
    {"id": 2, "titulo": "Studio compacto", "tags": ["compacto"], "preco": 250000},
    {"id": 3, "titulo": "Cobertura Luxo", "tags": ["varanda", "piscina", "luxo"], "preco": 900000} # Adicionado 'luxo'
]

# --- 4. SCHEMA DE EXTRAÇÃO ---
class LeadScoring(BaseModel):
    motivo_mudanca: str = Field(description="Resumo do evento de vida do usuário")
    urgencia: str = Field(description="Classificação: alta, media ou baixa")
    capacidade_financeira: str = Field(description="Classificação: alta, media ou baixa")
    tags: List[str] = Field(description="Interesses específicos, ex: varanda, pet-friendly")

# --- 5. O CÉREBRO (Processamento Completo) ---
def processar_lead(texto_usuario):
    # A. Extração (IA) usando response_schema para robustez
    prompt = f"""
    Analise o texto: '{texto_usuario}'. Extraia os dados do lead conforme o schema JSON.

    Extraia apenas o JSON.
    """
    response = model.generate_content(
        prompt,
        generation_config={
            "response_mime_type": "application/json",
            "response_schema": LeadScoring,
        },
    )
    raw_json = response.text.replace("```json", "").replace("```", "").strip()
    dados = json.loads(raw_json)

    # B. Recomendação (Lógica)
    sugestoes = [i for i in estoque if any(tag in i['tags'] for tag in dados['tags'])]

    # C. Scoring (Matemática de Negócio)
    score = PESOS['urgencia'].get(dados['urgencia'], 0) + \
            PESOS['capacidade_financeira'].get(dados['capacidade_financeira'], 0)
    # Aplica bônus por tags
    for tag in dados['tags']:
        if tag in ['luxo', 'cobertura']: # Mantendo 'cobertura' para uso futuro
            score += PESOS['bonus_tag_premium']

    temperatura = "QUENTE" if score >= 80 else ("MORNO" if score >= 40 else "FRIO")

    # D. Saída (Dossiê) - Adaptado para o formato do usuário
    sugestoes_titulos = [s['titulo'] for s in sugestoes]
    dossie_string = f"Lead: {dados['motivo_mudanca']} | Sugestões: {sugestoes_titulos}"

    return {
        "status": "sucesso",
        "score": score,
        "temperatura": temperatura,
        "dossie": dossie_string
    }

